package daytwelve.collections.set;

import java.util.LinkedHashSet;

public class LinkedHashSetDemo {

	public static void main(String[] args) {
		LinkedHashSet<String> lhs=new LinkedHashSet<String>();
		lhs.add("Disha");
		lhs.add("Sam");
		lhs.add("Karsha");
		lhs.add("Vishakha");
		lhs.add("Khushi");
		lhs.add("Pooja");
		lhs.add(null);
		System.out.println(lhs);
		
		

	}

}
